function event_say(e)
	if(e.message:findi("hail")) then
		e.self:Emote("turns from the skeletal musicians and glares at you, 'Mildly amusing, is it not?  I am not certain if their fate is a fortunate one or not - playing songs for all eternity.  It's a wonder they haven't been completely destroyed and turned to ash.  The Master despises minstrels.  He allows these little trophies to perform for the pleasure of his guests, so long as they do not get out of line.  Please, do not encourage them.  If they attempt their raucous antics again, there will be... trouble.");
	end
end
